class DashboardItem {
  final double size;
  final String date;
  final String time;
  final String address;
  final String typeOfWash;
  final String username;
  final String paymentStatus;

  DashboardItem({
    required this.size,
    required this.date,
    required this.time,
    required this.address,
    required this.typeOfWash,
    required this.username,
    required this.paymentStatus,
  });
}
